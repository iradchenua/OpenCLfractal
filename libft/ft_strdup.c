#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*res;
	int		i;

	res = (char *)malloc(ft_strlen(str) + 1);
	if (!res)
		return (NULL);
	i = 0;
	while (str[i])
	{
		res[i] = str[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
